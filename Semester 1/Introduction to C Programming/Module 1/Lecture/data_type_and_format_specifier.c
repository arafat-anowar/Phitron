// Data Type & Format specifier


#include<stdio.h>
#include<stdbool.h>

int main(){

    int a=10;
    printf("%d\n",a);

    long long int b=87459125447812;
    printf("%lld\n",b);

    float c=105.84;
    printf("%f\n",c);

    double d=5842542.4484512548451;
    printf("%lf\n",d);

    char e='a';
    printf("%c\n",e);

    bool f=false;
    printf("%d\n",f);

    char string[100]="Hello";
    printf("%s\n",string);

    return 0;
}